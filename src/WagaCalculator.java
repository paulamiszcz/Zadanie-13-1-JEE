public class WagaCalculator {
    public double kiloNaGram(double kilogram) {
        return kilogram*1000;
    }
    public double kiloNaMg(double kilogram) {
        return kilogram*1000000;
    }
    public double gramNaKilo(double gram) {
        return gram*0.001;
    }
    public double gramNaMg(double gram) {
        return gram*1000;
    }
    public double mgNaKilo(double mg) {
        return mg*0.000001;
    }
    public double mgNaGram(double mg) {
        return mg*0.001;
    }
}
